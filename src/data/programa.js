export function getTotalAssuntos() {
  return PROGRAMA.reduce((a, b) => a + b.materias.reduce((c, d) => c + d.assuntos.length, 0), 0);
}

export function getAllMaterias() {
  const all = [];
  PROGRAMA.forEach(bloco => {
    bloco.materias.forEach(mat => {
      all.push({ ...mat, blocoId: bloco.id, cargo: bloco.cargo });
    });
  });
  return all;
}
